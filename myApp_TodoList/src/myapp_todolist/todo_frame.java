    class clsTodos{
            public String todo;
            public String deadline;
            public String desc;
            public int todos;

        public clsTodos(int Todos, String Todo, String Deadline, String Desc) {
            this.todo = Todo;
            this.deadline = Deadline;
            this.desc = Desc;
            this.todos = Todos;
        }
        
        public void display(){
            for (int i = 0; i < this.todos; i++) {
                outputTodo.setText(this.todo);
                outputDeadline.setText(this.deadline);
                outputDesc.setText(this.desc);
            }
        }
            
            
    }

