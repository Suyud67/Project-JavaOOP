    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        int todos = Integer.parseInt(fieldTodos.getText());
        String todo = fieldTodo.getText();
        String deadline = fieldDeadline.getText();
        String desc = fieldDesc.getText();
        
        clsTodos[] arr = new clsTodos[todos];
        
        for (int i = 0; i < todos; i++) {
            arr[i] = new clsTodos(todos, todo, deadline, desc);
            arr[i].display();
        }
        
       
    }//GEN-LAST:event_btnSubmitActionPerformed

